function renderUpdates() {
    const modal = document.getElementById('updatesModal');
    let html = `
        <span style="float:right;font-weight:bold;cursor:pointer" onclick="game.closeModal('updatesModal')">[ЗАКРЫТЬ]</span>
        <div style="text-align:center; margin-bottom:20px;">
            <h2 style="color:var(--accent); margin:0; font-size:1.3rem; letter-spacing:2px;">PATCH NOTES</h2>
            <small style="color:var(--gray);">История обновлений SkyBlock RPG</small>
        </div>
    `;

    updatesList.forEach((update, idx) => {
        const isFirst = idx === 0;
        html += `
            <div style="
                background: linear-gradient(165deg, ${isFirst ? '#1e1e2a' : '#16161a'} 0%, #12121a 100%);
                border: 1px solid ${isFirst ? 'rgba(255,170,0,0.2)' : 'rgba(255,255,255,0.05)'};
                border-radius: 12px;
                padding: 16px;
                margin-bottom: 12px;
                ${isFirst ? 'box-shadow: 0 0 20px rgba(255,170,0,0.05);' : ''}
            ">
                <div style="display:flex; align-items:center; gap:8px; margin-bottom:10px; flex-wrap:wrap;">
                    <span style="
                        background: rgba(255,255,255,0.08);
                        border: 1px solid rgba(255,255,255,0.15);
                        color: var(--white);
                        padding: 3px 10px;
                        border-radius: 6px;
                        font-size: 0.75rem;
                        font-weight: 800;
                        letter-spacing: 0.5px;
                    ">${update.version}</span>
                    <span style="
                        background: ${update.tagColor}22;
                        border: 1px solid ${update.tagColor}44;
                        color: ${update.tagColor};
                        padding: 3px 8px;
                        border-radius: 6px;
                        font-size: 0.6rem;
                        font-weight: 800;
                        letter-spacing: 1px;
                        text-transform: uppercase;
                    ">${update.tag}</span>
                    <span style="color:var(--gray); font-size:0.7rem; margin-left:auto;">${update.date}</span>
                </div>
                <h4 style="color:var(--white); margin:0 0 10px 0; font-size:0.95rem;">${update.title}</h4>
                <div style="display:flex; flex-direction:column; gap:4px;">
        `;

        update.changes.forEach(change => {
            html += `
                <div style="display:flex; gap:8px; align-items:flex-start;">
                    <span style="color:${update.tagColor}; font-size:0.6rem; margin-top:4px; flex-shrink:0;">●</span>
                    <span style="color:var(--gray); font-size:0.78rem; line-height:1.5;">${change}</span>
                </div>
            `;
        });

        html += `</div>`;

        if (update.subsections) {
            update.subsections.forEach(sub => {
                html += `
                    <div style="
                        margin-top:12px;
                        padding:10px 12px;
                        background: rgba(0,0,0,0.25);
                        border-radius: 8px;
                        border-left: 3px solid ${update.tagColor};
                    ">
                        <h5 style="margin:0 0 8px 0; color:var(--accent); font-size:0.8rem;">${sub.icon || '📌'} ${sub.title}</h5>
                        <div style="display:flex; flex-direction:column; gap:3px;">
                `;
                sub.items.forEach(item => {
                    html += `
                        <div style="display:flex; gap:6px; align-items:flex-start;">
                            <span style="color:var(--gray); font-size:0.55rem; margin-top:4px; flex-shrink:0;">▸</span>
                            <span style="color:var(--gray); font-size:0.72rem; line-height:1.4;">${item}</span>
                        </div>
                    `;
                });
                html += `</div></div>`;
            });
        }

        html += `</div>`;
    });

    modal.innerHTML = html;
}
